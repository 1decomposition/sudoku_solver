#include <unistd.h>

void	print(char *arr[])
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (i < 9)
	{
		while (j < 9)
		{
			write(1, &arr[i][j], 1);
			j++;
			if (j != 9)
				write(1, " ", 1);
		}
		write(1, "\n", 1);
		i++;
		j = 0;
	}
}
