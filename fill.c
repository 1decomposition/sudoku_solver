void	fill(char **arr, char **argv)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (i < 9)
	{
		while (j < 9)
		{
			arr[i][j] = argv[i + 1][j];
			j++;
		}
		i++;
		j = 0;
	}
}
